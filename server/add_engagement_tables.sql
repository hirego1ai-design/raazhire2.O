-- 4. ENABLE RLS
ALTER TABLE conversations ENABLE ROW LEVEL SECURITY;
ALTER TABLE messages ENABLE ROW LEVEL SECURITY;

-- 5. RLS POLICIES
-- Conversations
CREATE POLICY "Users can view their own conversations" ON conversations
    FOR SELECT USING (auth.uid() = candidate_id OR auth.uid() = employer_id);

CREATE POLICY "Users can insert conversations they are part of" ON conversations
    FOR INSERT WITH CHECK (auth.uid() = candidate_id OR auth.uid() = employer_id);

CREATE POLICY "Users can update their own conversations" ON conversations
    FOR UPDATE USING (auth.uid() = candidate_id OR auth.uid() = employer_id);

-- Messages
CREATE POLICY "Users can view messages in their conversations" ON messages
    FOR SELECT USING (
        EXISTS (
            SELECT 1 FROM conversations c 
            WHERE c.id = messages.conversation_id 
            AND (c.candidate_id = auth.uid() OR c.employer_id = auth.uid())
        )
    );

CREATE POLICY "Users can send messages to their conversations" ON messages
    FOR INSERT WITH CHECK (
        EXISTS (
            SELECT 1 FROM conversations c 
            WHERE c.id = conversation_id 
            AND (c.candidate_id = auth.uid() OR c.employer_id = auth.uid())
        )
        AND sender_id = auth.uid()
    );

